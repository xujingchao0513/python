#	若要提出Python语言修改建议，需要编写Python改进提案（Python	Enhancement Proposal，PEP）。
#	PEP 8是最古老的PEP之一，它向Python程序员提供了代码格式设置指南

#	缩进
#	PEP 8建议每级缩进都使用四个空格，这即可提高可读性，又留下了足够的多级缩进空间

#	行长
#	建议每行不超过80字符，最初指定这样的指南时，在大多数计算机中，终端窗口每行只能容纳79字符；
#	注释行长不超过72字符；

#	空行
#	例如：如果你有5行创建列表的代码，还有3行处理列表的代码，那么用一个空行将两部分分隔开是合适的。
